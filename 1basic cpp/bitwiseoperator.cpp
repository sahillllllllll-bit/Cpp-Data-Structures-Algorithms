#include<iostream>
using namespace std;
int main(){
 int a=4,b=2;

    ///                                                                        "&" operator 

    cout<<(a&b)<<endl;



     //                                                                     "|" operator
     cout<<(a|b)<<endl;





     //                                                                    "^ " xor operator
     cout<<(a^b)<<endl;
    
    
    
     //                                                             "<<" left shift operator
     cout<<(a<<b)<<endl;
    
    
    
     //                                                                 ">>" right shift operator
     cout<<(a>>b)<<endl;
    }

 